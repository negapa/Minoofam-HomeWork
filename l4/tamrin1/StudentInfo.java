package l4.tamrin1;

public class StudentInfo {
    private int studentID;
    private String name;
    private double GPA;

    public String getName() {return name;}
    public void setName(String name) {this.name = name;}

    public int getNumber() {return studentID;}
    public void setNumber(int studentID) {this.studentID = studentID;}

    public double getScore() {return GPA;}
    public void setScore(double GPA) {this.GPA = GPA;}
}

